select -- Себестоимость единицы продукции (расчёт)
    SUM(sm.[Количество_материала] * cm.[Цена]) AS Себестоимость_единицы,
    
    -- Полная себестоимость позиции (количество в заказе × себестоимость единицы)
    sz.[Количество] * SUM(sm.[Количество_материала] * cm.[Цена]) AS Себестоимость_позиции
    
FROM [dbo].[Заказы] z
JOIN [dbo].[Состав_заказа] sz ON sz.[№_заказа] = z.[№_заказа]
JOIN [dbo].[Продукция] p ON p.[№_продукции] = sz.[Продукция]
JOIN [dbo].[Спецификация] s ON s.[№_продукции] = p.[№_продукции]
JOIN [dbo].[Спецификация_материал] sm ON sm.[№_спецификации] = s.[№_спецификации]
JOIN [dbo].[Материалы] m ON m.[№_материала] = sm.[№_материала]
JOIN [dbo].[Цены_материалов] cm ON cm.[№_материал] = m.[№_материала]
JOIN [dbo].[Цены_продукции] cp ON cp.[№_продукции] = p.[№_продукции]

GROUP BY 
    z.[№_заказа],
    z.[Заказчик],
    z.[Исполнитель],
    sz.[№_заказа_покупателя],
    p.[Наименование],
    sz.[Количество],
    cp.[Цена]